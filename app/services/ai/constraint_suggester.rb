module Ai
  # ユーザーの目標に対し、達成のための「試練（制約）」案を 3 つ生成する。
  #
  # バリエーション拡充ポイント:
  # - 制約タイプを 4 軸（時間 / 場所 / 環境 / 行動）で分類して few-shot プールを保持
  # - 各タイプから例を毎回ランダム抽出してプロンプトに添える
  # - 「過去出力と被らないよう新しい角度で」を明示
  class ConstraintSuggester < BaseSuggester
    # 時間制約: 「N 時以降〜しない」「朝・夜の N 分は〜」
    EXAMPLES_TIME = [
      "夜 22 時以降スマホを触らない",
      "起床後 30 分はメールを見ない",
      "毎週日曜は仕事をしない",
      "21 時以降カフェインを摂らない",
      "ランチ前にニュースサイトを開かない",
      "就寝 1 時間前は画面を見ない"
    ].freeze

    # 場所制約: 「ベッドで〜しない」「カフェでは〜」
    EXAMPLES_PLACE = [
      "ベッドの上で SNS を見ない",
      "通勤電車では本以外を読まない",
      "リビングではゲームしない",
      "カフェでは仕事の Slack を見ない",
      "トイレにスマホを持ち込まない"
    ].freeze

    # 環境制約: 「家にお菓子を置かない」「通知を切る」
    EXAMPLES_ENV = [
      "家にお菓子を置かない",
      "通知バッジを 1 日 1 回しか確認しない",
      "デスクに飲み物以外置かない",
      "アプリのトップ画面に SNS を置かない",
      "テレビのリモコンを別部屋に置く"
    ].freeze

    # 行動制約: 「言い訳の前に行動する」「グチを言わない」など
    EXAMPLES_BEHAVIOR = [
      "言い訳を 3 つ並べる前に行動する",
      "1 日 1 回より先にグチを言わない",
      "返信前に 1 度深呼吸する",
      "断る前に 1 日考える",
      "完璧を目指す前にまず 5 分だけ取り掛かる",
      "判断を先延ばしにしない（即決する）"
    ].freeze

    def suggest(goal:)
      examples = sample_examples
      result = chat_json(build_prompt(goal, examples))
      Array(result["constraints"]).first(3)
    end

    private

    # 4 タイプから 1 例ずつランダム抽出。プロンプトに毎回違う例を入れる。
    def sample_examples
      {
        time: EXAMPLES_TIME.sample,
        place: EXAMPLES_PLACE.sample,
        env: EXAMPLES_ENV.sample,
        behavior: EXAMPLES_BEHAVIOR.sample
      }
    end

    def build_prompt(goal, examples)
      <<~PROMPT
        あなたはユーザーの自己鍛錬を支援する AI です。
        ユーザーは「#{goal}」という目標を立てました。
        この目標達成のために自らに課す「制約（やらないこと / 控えること）」を 3 つ提案してください。

        ヒント例（参考。これらをそのまま使わず別の発想で出すこと）:
        - 時間制約: #{examples[:time]}
        - 場所制約: #{examples[:place]}
        - 環境制約: #{examples[:env]}
        - 行動制約: #{examples[:behavior]}

        意識してほしいこと:
        - 1 つは「王道で効果が高い」制約
        - 1 つは「ちょっと変わっていてニヤッとする」制約
        - 1 つは「斬新で達成感が出る」制約
        - 制約のタイプ（時間/場所/環境/行動）を意識し、3 案で軸を散らす

        条件:
        - 現代日本語の自然な表現（中世ファンタジー風の表現は使わない）
        - 1 文（30 文字以内）
        - 毎日実行できる具体性
        - 真面目すぎず、ユーモアや意外性も歓迎（ただし下品・攻撃的なものは避ける）
        - 3 案でトーンが被らないようにする
        - 上のヒント例の言い回しを使い回さない（参考にしつつも別の言葉で）

        以下の JSON 形式で返してください:
        {"constraints": ["王道案", "ひねり案", "斬新案"]}
      PROMPT
    end
  end
end
