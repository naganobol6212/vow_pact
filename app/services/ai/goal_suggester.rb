module Ai
  # ユーザーが入力したテーマに対し、自己鍛錬の目標案を 3 つ生成する。
  # theme が空文字 / nil の場合は「おまかせ（ランダム）モード」として、
  # 4 タイプ（突拍子もない / ストレッチ / 面白い / 自己成長）からミックスして提案する。
  class GoalSuggester < BaseSuggester
    def suggest(theme:)
      prompt = theme.to_s.strip.empty? ? build_random_prompt : build_themed_prompt(theme)
      result = chat_json(prompt)
      Array(result["goals"]).first(3)
    end

    private

    def build_themed_prompt(theme)
      <<~PROMPT
        あなたはユーザーの自己鍛錬を支援する AI です。
        ユーザーが「#{theme}」というテーマで自己鍛錬の目標を立てたいと考えています。
        具体的で実行可能な目標案を 3 つ提案してください。

        条件:
        - 1 つは「王道だけど確実に効く」目標
        - 1 つは「ちょっと変わっていて、思わずクスッとくる」目標
          （例: 毎朝鏡の自分を褒める / 通勤中に英語で独り言）
        - 1 つは「斬新で人に話したくなる」目標
          （例: 月 1 人新しい人と話す / 毎日 1 個ありがとうを記録）
        - 現代日本語の自然な表現（中世ファンタジー風の表現は使わない）
        - 1 文（30 文字以内）
        - 数値や頻度を含めて行動を明確に
        - 3 つで毛色が被らないようにする

        以下の JSON 形式で返してください:
        {"goals": ["王道案", "ひねり案", "斬新案"]}
      PROMPT
    end

    def build_random_prompt
      <<~PROMPT
        あなたはユーザーの自己鍛錬を支援する AI です。
        ユーザーはテーマを指定していません。意外性のある「おまかせ」目標案を 3 つ提案してください。

        以下の 4 タイプから 3 つを選び、それぞれ毛色が違うものになるよう意識してください:
        - 突拍子もないが実は実行可能（例：1 ヶ月毎日違う言語の挨拶を覚える / 毎日寝る前に明日の自分にメモ）
        - クスッと笑える系（例：毎朝鏡に向かって「俺は最高」と言う / 毎日違う道で帰宅）
        - 自分を高めるストレッチ（例：朝 5 時起きを 30 日続ける / 月 1 冊未経験ジャンルの本）
        - 人に話したくなる斬新さ（例：月 1 人新しい人と話す / 毎日その日の発見を 1 つ記録）

        条件:
        - 現代日本語の自然な表現（中世ファンタジー風の表現は使わない）
        - 1 文（30 文字以内）
        - 数値や頻度を含めて行動を明確に
        - 真面目すぎず、ユーモアや意外性も歓迎（ただし下品・攻撃的なものは避ける）
        - 3 案でトーンが被らないようにする

        以下の JSON 形式で返してください:
        {"goals": ["目標案 1", "目標案 2", "目標案 3"]}
      PROMPT
    end
  end
end
