module Ai
  # ユーザーが入力したテーマに対し、自己鍛錬の目標案を 3 つ生成する。
  #
  # 目標バリエーション拡充ポイント:
  # - **few-shot 例文プール**: タイプごとに 8〜12 個の例を持ち、毎回ランダムに 2 個ずつ抽出。
  #   プロンプトに入れる例が毎回変わることで、LLM が「いつもの 3 例」に引きずられて
  #   同じ出力を返すのを防ぐ。
  # - **ジャンル指定**: 学習 / 健康 / 創造 / 社交 / 内省 / 仕事 / 生活 から 1 つ選ぶと
  #   その方向に偏らせる（任意・空ならジャンル横断）。
  # - **指示の毎回ブレ**: ジャンル / 例文の組み合わせで同一テーマでも毎回違う出力を狙う。
  class GoalSuggester < BaseSuggester
    # ユーザーが選べるジャンル。空文字は「ジャンル指定なし」として扱う。
    SUPPORTED_GENRES = %w[学習 健康 創造 社交 内省 仕事 生活].freeze

    # 王道タイプの例（実用性 + 効果実績重視）
    EXAMPLES_ORTHODOX = [
      "毎日 30 分読書する",
      "週 3 回 30 分ランニング",
      "毎朝 6 時に起きる",
      "1 日 8 時間眠る",
      "毎食野菜を 1 皿食べる",
      "週 1 回 30 分の瞑想",
      "1 日 1 万歩歩く",
      "毎週末に翌週の予定を書く",
      "1 日 1 杯の白湯を飲む",
      "毎晩 5 分ストレッチ",
      "週 5 日 30 分散歩する",
      "毎月 1 冊ビジネス書を読む"
    ].freeze

    # ひねりタイプの例（クスッと笑える / 意外性）
    EXAMPLES_TWIST = [
      "毎朝鏡の自分を褒める",
      "通勤中に英語で独り言",
      "1 日 1 個ありがとうを記録",
      "毎日違う道で帰宅する",
      "週 1 回新しいカフェに行く",
      "毎晩寝る前に明日の自分にメモ",
      "毎日 1 つ初めての言葉を覚える",
      "1 週間ごとに違う色を身につける",
      "毎日空を 1 分眺める",
      "週 1 回手紙を 1 通書く",
      "毎朝違う匂いの香水をつける",
      "毎日 1 つ自分にダジャレを言う"
    ].freeze

    # 斬新タイプの例（人に話したくなる / コミュニティ性）
    EXAMPLES_NOVEL = [
      "月 1 人新しい人と話す",
      "1 ヶ月毎日違う言語の挨拶を覚える",
      "毎日その日の発見を 1 つ記録",
      "週 1 回知らない料理を作る",
      "月 1 回新しい習い事を体験する",
      "毎週末に 1 つ新ジャンルの音楽を聴く",
      "月に 1 度知らない街で迷子になる",
      "毎週違うジャンルの映画を観る",
      "毎月 1 回美術館か博物館に行く",
      "週 1 回違う SNS の人にコメントする"
    ].freeze

    # ジャンル別のヒント文。プロンプトに添えて方向性を寄せる。
    GENRE_HINTS = {
      "学習" => "新しい知識・スキル習得（語学・読書・資格・プログラミング等）",
      "健康" => "身体作り・睡眠・食事・運動・メンタルケア",
      "創造" => "創作・表現（文章・絵・音楽・写真・動画・工作等）",
      "社交" => "人との関わり・コミュニケーション・新しい出会い",
      "内省" => "自己理解・瞑想・日記・感情整理・価値観の見直し",
      "仕事" => "業務改善・キャリア・副業・プロフェッショナル研鑽",
      "生活" => "整理整頓・家事・節約・暮らしの質向上"
    }.freeze

    def suggest(theme:, genre: nil)
      genre_clean = genre.to_s.strip
      genre_clean = nil unless SUPPORTED_GENRES.include?(genre_clean)

      examples = sample_examples
      prompt = theme.to_s.strip.empty? ? build_random_prompt(examples, genre_clean) : build_themed_prompt(theme, examples, genre_clean)
      result = chat_json(prompt)
      Array(result["goals"]).first(3)
    end

    private

    # 各タイプから 2 例ずつランダム抽出。プロンプトに入れる few-shot を毎回変えるため。
    def sample_examples
      {
        orthodox: EXAMPLES_ORTHODOX.sample(2),
        twist: EXAMPLES_TWIST.sample(2),
        novel: EXAMPLES_NOVEL.sample(2)
      }
    end

    def genre_clause(genre)
      return "" if genre.nil?
      "- このジャンルに寄せる：#{genre}（#{GENRE_HINTS[genre]}）\n        "
    end

    def build_themed_prompt(theme, examples, genre)
      <<~PROMPT
        あなたはユーザーの自己鍛錬を支援する AI です。
        ユーザーが「#{theme}」というテーマで自己鍛錬の目標を立てたいと考えています。
        具体的で実行可能な目標案を 3 つ提案してください。

        ヒント例（参考。これらをそのまま使わず別の発想で出すこと）:
        - 王道: #{examples[:orthodox].join(' / ')}
        - ひねり: #{examples[:twist].join(' / ')}
        - 斬新: #{examples[:novel].join(' / ')}

        条件:
        - 1 つは「王道だけど確実に効く」目標
        - 1 つは「ちょっと変わっていて、思わずクスッとくる」目標
        - 1 つは「斬新で人に話したくなる」目標
        #{genre_clause(genre)}- 現代日本語の自然な表現（中世ファンタジー風の表現は使わない）
        - 1 文（30 文字以内）
        - 数値や頻度を含めて行動を明確に
        - 3 つで毛色が被らないようにする
        - 上のヒント例の言い回しを使い回さない（参考にしつつも別の言葉で）

        以下の JSON 形式で返してください:
        {"goals": ["王道案", "ひねり案", "斬新案"]}
      PROMPT
    end

    def build_random_prompt(examples, genre)
      <<~PROMPT
        あなたはユーザーの自己鍛錬を支援する AI です。
        ユーザーはテーマを指定していません。意外性のある「おまかせ」目標案を 3 つ提案してください。

        ヒント例（参考。これらをそのまま使わず別の発想で出すこと）:
        - 王道: #{examples[:orthodox].join(' / ')}
        - ひねり: #{examples[:twist].join(' / ')}
        - 斬新: #{examples[:novel].join(' / ')}

        以下の 4 タイプから 3 つを選び、それぞれ毛色が違うものになるよう意識してください:
        - 突拍子もないが実は実行可能
        - クスッと笑える系
        - 自分を高めるストレッチ
        - 人に話したくなる斬新さ

        条件:
        #{genre_clause(genre)}- 現代日本語の自然な表現（中世ファンタジー風の表現は使わない）
        - 1 文（30 文字以内）
        - 数値や頻度を含めて行動を明確に
        - 真面目すぎず、ユーモアや意外性も歓迎（ただし下品・攻撃的なものは避ける）
        - 3 案でトーンが被らないようにする
        - 上のヒント例の言い回しを使い回さない（参考にしつつも別の言葉で）

        以下の JSON 形式で返してください:
        {"goals": ["目標案 1", "目標案 2", "目標案 3"]}
      PROMPT
    end
  end
end
